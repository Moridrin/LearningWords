/**
 * This enum is for the Test Type. There are three types (Main to Language, Language To Main, and Both). The "Main to
 * Language" test will ask only words in the main language, and you have to give the translations. The "Language to
 * Main" will do it in the exact opposite, and the "Both" will obviously ask in both directions.
 *
 * @organization: Moridrin
 * @author J.B.A.J. Berkvens
 * @date 2014/04/14
 */
public enum TestType {

    MainToLanguage,
    LanguageToMain,
    Both;
}
