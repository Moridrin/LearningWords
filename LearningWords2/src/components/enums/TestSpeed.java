/**
 * This enum is for the Test Speed. Currently it has two speeds (Good and Fast). The "Good" test will ask all words in
 * the list in random order, and if you have a word wrong it will be added to a separate list that will be looped when
 * all questions in the main list have been answered correctly. The "Fast" test will just loop all words in the list in
 * random order.
 *
 * @organization: Moridrin
 * @author J.B.A.J. Berkvens
 * @date 2014/04/14
 */
public enum TestSpeed {

    Good,
    Fast;
}
